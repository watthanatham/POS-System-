    public void SearchUser() {
        UserDao dao = new UserDao();
        userList = dao.getSearch(txtSearch.getText());
        if (userList.isEmpty()) {
            lodeTable(dao);
        } else {
            model = new UserTableModel(userList);
            tblUser.setModel(model);
        }
    }
